Map::Map() : grid(map_size * map_size)
{
    for (int x = 0; x < map_size; ++x)
    {
        for (int y = 0; y < map_size; ++y)
        {
            grid[x * map_size + y] = initialGrid[x][y];
        }
    }
}

int Map::at(int x, int y) const
{
    return grid[x * map_size + y];
}
